    private void ApplyAutoGeneratedInstanceName(bool force)
    {
        var suggestedName = CreateSuggestedInstanceName();
        if (string.IsNullOrWhiteSpace(suggestedName))
            return;

        var currentName = InstanceName?.Trim() ?? string.Empty;
        var shouldApply = force
            || string.IsNullOrWhiteSpace(currentName)
            || string.Equals(currentName, lastAutoGeneratedInstanceName, StringComparison.OrdinalIgnoreCase);

        if (!shouldApply)
            return;

        isUpdatingInstanceNameInternally = true;
        try
        {
            InstanceName = suggestedName;
            lastAutoGeneratedInstanceName = suggestedName;
        }
        finally
        {
            isUpdatingInstanceNameInternally = false;
        }
    }

    private string CreateSuggestedInstanceName()
    {
        var minecraftVersionName = SelectedMinecraftVersion?.Name?.Trim();
        if (string.IsNullOrWhiteSpace(minecraftVersionName))
            return string.Empty;

        return SelectedLoaderOption?.Kind switch
        {
            LoaderKind.Fabric when !string.IsNullOrWhiteSpace(SelectedLoaderVersion?.Version)
                => $"{minecraftVersionName}-fabric-{SelectedLoaderVersion.Version}",
            LoaderKind.Fabric => $"{minecraftVersionName}-fabric",
            LoaderKind.Forge when !string.IsNullOrWhiteSpace(SelectedLoaderVersion?.Version)
                => $"{minecraftVersionName}-forge-{SelectedLoaderVersion.Version}",
            LoaderKind.Forge => $"{minecraftVersionName}-forge",
            LoaderKind.NeoForge when !string.IsNullOrWhiteSpace(SelectedLoaderVersion?.Version)
                => $"{minecraftVersionName}-neoforge-{SelectedLoaderVersion.Version}",
            LoaderKind.NeoForge => $"{minecraftVersionName}-neoforge",
            LoaderKind.Quilt when !string.IsNullOrWhiteSpace(SelectedLoaderVersion?.Version)
                => $"{minecraftVersionName}-quilt-{SelectedLoaderVersion.Version}",
            LoaderKind.Quilt => $"{minecraftVersionName}-quilt",
            _ => minecraftVersionName
        };
    }

    private string GetSelectedLoaderDisplayName()
    {
        var selectedLoaderVersion = SelectedLoaderVersion?.Version;
        if (RequiresLoaderVersionSelection(SelectedLoaderOption?.Kind) && !string.IsNullOrWhiteSpace(selectedLoaderVersion))
            return $"{SelectedLoaderOption?.Title ?? Strings.Download_VanillaLoaderTitle} {selectedLoaderVersion}";

        return SelectedLoaderOption?.Title ?? Strings.Download_VanillaLoaderTitle;
    }

