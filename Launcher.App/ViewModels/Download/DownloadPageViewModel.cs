    [RelayCommand(CanExecute = nameof(CanInstall), AllowConcurrentExecutions = true)]
    private async Task InstallAsync()
    {
        if (!CanInstall() || SelectedMinecraftVersion is null)
            return;

        var versionName = SelectedMinecraftVersion.Name;
        var instanceName = string.IsNullOrWhiteSpace(InstanceName) ? versionName : InstanceName.Trim();
        var selectedLoader = SelectedLoaderOption ?? LoaderOptions.First(option => option.Kind is LoaderKind.Vanilla);
        var loaderVersion = selectedLoader.Kind is LoaderKind.Vanilla ? null : SelectedLoaderVersion?.Version;
        var fabricApiVersionId = selectedLoader.Kind is LoaderKind.Fabric
            ? SelectedAddonLibraryVersion?.VersionId
            : null;
        var quiltStandardLibraryVersionId = selectedLoader.Kind is LoaderKind.Quilt
            ? SelectedAddonLibraryVersion?.VersionId
            : null;
        var loaderDisplayName = GetSelectedLoaderDisplayName();
        var installSequence = Interlocked.Increment(ref latestInstallSequence);
        var installTask = downloadTasksPage.BeginTask($"{loaderDisplayName} {versionName}", instanceName);
        floatingMessageService.Show(Strings.Status_InstallStartingDownload);

        var activeInstallCountAfterStart = Interlocked.Increment(ref activeInstallCount);
        IsInstalling = activeInstallCountAfterStart > 0;
        InstallError = string.Empty;
        InstallProgressPercent = 0;
        InstallStatusMessage = Strings.Status_InstallPreparing;
        installTask.Report(new LauncherProgress(InstallProgressStages.Preparing, string.Empty, 0));
        instanceNameTracker.AddPending(instanceName);
        RefreshInstanceNameDuplicateMessage();
        CurrentStep = DownloadPageStep.VersionList;
        NotifyInstallStateChanged();
        GoToInstanceOptionsCommand.NotifyCanExecuteChanged();
        RequestVisibleVersionsRefresh(defer: true);

        try
        {
            using var installProgress = CreateProgress(installTask, installSequence);
            var instance = await instanceService.CreateInstanceAsync(
                versionName,
                selectedLoader.Kind,
                loaderVersion,
                instanceName,
                installProgress,
                installTask.CancellationToken,
                downloadSourcePreference: downloadSourcePreference,
                downloadSpeedLimitMbPerSecond: downloadSpeedLimitMbPerSecond,
                installFabricApi: fabricApiVersionId is not null,
                fabricApiVersionId: fabricApiVersionId,
                quiltStandardLibraryVersionId: quiltStandardLibraryVersionId);

            instanceNameTracker.RemovePending(instanceName);
            SetLatestInstallCompletion(installSequence, string.Format(Strings.Status_InstanceInstalledFormat, instance.Name));
            instanceNameTracker.AddExisting(instance.Name);
            instanceNameTracker.AddExisting(instance.VersionName);
            installTask.Complete(string.Format(Strings.Status_InstanceInstalledFormat, instance.Name));
            InstanceInstalled?.Invoke(this, instance);
        }
        catch (OperationCanceledException) when (installTask.IsCancellationRequested)
        {
            instanceNameTracker.RemovePending(instanceName);
            if (installSequence == latestInstallSequence)
            {
                InstallError = string.Empty;
                InstallStatusMessage = string.Empty;
                InstallProgressPercent = 0;
            }

            downloadTasksPage.CancelTask(installTask);
        }
        catch (DuplicateGameInstanceNameException)
        {
            instanceNameTracker.RemovePending(instanceName);
            SetLatestInstallFailure(installSequence, Strings.Status_DuplicateInstanceName);
            installTask.Fail(Strings.Status_DuplicateInstanceName);
        }
        catch (Exception)
        {
            instanceNameTracker.RemovePending(instanceName);
            SetLatestInstallFailure(installSequence, Strings.Status_InstallFailed);
            installTask.Fail(Strings.Status_InstallFailed);
        }
        finally
        {
            var activeInstallCountAfterFinish = Interlocked.Decrement(ref activeInstallCount);
            if (activeInstallCountAfterFinish < 0)
            {
                Interlocked.Exchange(ref activeInstallCount, 0);
                activeInstallCountAfterFinish = 0;
            }

            IsInstalling = activeInstallCountAfterFinish > 0;
            RefreshInstanceNameDuplicateMessage();
        }
    }

    private bool CanInstall()
    {
        var canInstallWithSelectedLoader = SelectedLoaderOption?.Kind switch
        {
            LoaderKind.Vanilla => true,
            { } loaderKind when RequiresLoaderVersionSelection(loaderKind) => !IsLoadingLoaderVersions
                && (!SupportsAddonLibrarySelection(loaderKind) || !IsLoadingAddonLibraryVersions)
                && string.IsNullOrWhiteSpace(LoaderVersionLoadError)
                && SelectedLoaderVersion is not null,
            _ => false
        };

        return CurrentStep is DownloadPageStep.InstanceOptions
            && SelectedMinecraftVersion is not null
            && canInstallWithSelectedLoader
            && !string.IsNullOrWhiteSpace(InstanceName)
            && !HasInstanceNameDuplicateMessage;
    }

