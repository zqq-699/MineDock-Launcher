    public void ApplyDownloadSourcePreference(DownloadSourcePreference preference)
    {
        if (downloadSourcePreference == preference)
            return;

        downloadSourcePreference = preference;
        LocalImportDialog.ApplyDownloadSourcePreference(preference);
        hasLoadedVersions = false;
        IsLoadingVersions = false;
        VersionLoadError = string.Empty;
        VersionEmptyMessage = string.Empty;
        AllVersions.Clear();
        ClearSelectedVersion();
        RefreshVisibleVersions();

        if (CurrentStep is DownloadPageStep.InstanceOptions
            && RequiresLoaderVersionSelection(SelectedLoaderOption?.Kind)
            && SelectedMinecraftVersion is not null)
        {
            _ = RefreshLoaderVersionsForSelectionAsync(SelectedLoaderOption);
            _ = RefreshAddonLibraryVersionsForSelectionAsync(SelectedLoaderOption);
        }
    }

    public void ApplyDownloadSpeedLimit(int downloadSpeedLimitMbPerSecond)
    {
        this.downloadSpeedLimitMbPerSecond = Math.Max(downloadSpeedLimitMbPerSecond, 0);
        LocalImportDialog.ApplyDownloadSpeedLimit(this.downloadSpeedLimitMbPerSecond);
    }

    public bool CanHandleLocalImportDrop(IReadOnlyList<string> paths)
