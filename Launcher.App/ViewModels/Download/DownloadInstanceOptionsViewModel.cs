    /// <summary>
    /// 获取当前 Loader 可用版本，且只允许仍匹配当前页面选择的请求更新集合。
    /// </summary>
    private async Task RefreshLoaderVersionsForSelectionAsync(DownloadLoaderOption? loaderOption)
    {
        // 除取消标记外还校验 loader 与 Minecraft 版本，覆盖提供方忽略 CancellationToken 的情况。
        var cancellation = ReplaceLoaderRequestCancellation();
        if (!RequiresLoaderVersionSelection(loaderOption?.Kind)
            || selectedMinecraftVersion is null
            || !isActive)
        {
            ResetLoaderVersionState(cancelRequest: false);
            ReleaseLoaderCancellation(cancellation);
            return;
        }

        if (!loaderProviders.TryGetValue(loaderOption!.Kind, out var provider) || !provider.IsImplemented)
        {
            ResetLoaderVersionState(cancelRequest: false);
            ReleaseLoaderCancellation(cancellation);
            return;
        }

        var requestedVersion = selectedMinecraftVersion.Name;
        LoaderVersionLoadError = string.Empty;
        IsLoadingLoaderVersions = true;
        LoaderVersions.Clear();
        SelectedLoaderVersion = null;
        try
        {
            var versions = await provider.GetLoaderVersionsAsync(
                requestedVersion,
                downloadSourcePreference,
                cancellation.Token,
                downloadSpeedLimitMbPerSecond);
            // 提供方可能在取消后仍返回，因此还要比对 CTS、Loader 对象和 Minecraft 版本。
            if (!IsLoaderRequestCurrent(cancellation, loaderOption, requestedVersion))
                return;
            LoaderVersions.ReplaceWith(versions);
            SelectedLoaderVersion = null;
            ApplyAutoGeneratedInstanceName(force: false);
        }
        catch (OperationCanceledException) when (cancellation.IsCancellationRequested)
        {
        }
        catch (Exception exception)
        {
            logger.LogWarning(
                exception,
                "Failed to load loader versions. MinecraftVersion={MinecraftVersion} Loader={Loader}",
                requestedVersion,
                loaderOption.Kind);
            if (IsLoaderRequestCurrent(cancellation, loaderOption, requestedVersion))
                LoaderVersionLoadError = string.Format(Strings.Status_LoaderVersionsLoadFailedFormat, loaderOption.Title);
        }
        finally
        {
            if (IsLoaderRequestCurrent(cancellation, loaderOption, requestedVersion))
                IsLoadingLoaderVersions = false;
            ReleaseLoaderCancellation(cancellation);
        }
    }

    /// <summary>
    /// 获取 Fabric API 或 Quilt 标准库版本，并为“无需安装”保留显式选项。
    /// </summary>
    private async Task RefreshAddonLibraryVersionsForSelectionAsync(DownloadLoaderOption? loaderOption)
    {
        // 可选库刷新与 loader 列表互不占用取消源，防止一个请求完成时误释放另一个请求。
        var cancellation = ReplaceAddonRequestCancellation();
        if (!SupportsAddonLibrarySelection(loaderOption?.Kind)
            || selectedMinecraftVersion is null
            || !isActive)
        {
            ResetAddonLibraryVersionState(cancelRequest: false);
            ReleaseAddonCancellation(cancellation);
            return;
        }

        var requestedVersion = selectedMinecraftVersion.Name;
        AddonLibraryVersionLoadError = string.Empty;
        IsLoadingAddonLibraryVersions = true;
        ClearAddonLibraryVersionOptions();
        if (modrinthService is null)
        {
            IsLoadingAddonLibraryVersions = false;
            ReleaseAddonCancellation(cancellation);
            return;
        }

        try
        {
            var versions = await GetAddonLibraryVersionsAsync(loaderOption!.Kind, requestedVersion, cancellation.Token);
            if (!IsAddonRequestCurrent(cancellation, loaderOption, requestedVersion))
                return;
            // “不安装附加库”必须是明确选项，不能用 null 同时表示加载中和用户不安装。
            AddonLibraryVersions.Add(DownloadAddonLibraryVersionOption.None);
            var isLatest = true;
            foreach (var version in versions)
            {
                AddonLibraryVersions.Add(DownloadAddonLibraryVersionOption.FromVersion(version, isLatest));
                isLatest = false;
            }

            SelectedAddonLibraryVersion = AddonLibraryVersions.FirstOrDefault(option => option.IsInstallable)
                ?? AddonLibraryVersions.FirstOrDefault();
        }
        catch (OperationCanceledException) when (cancellation.IsCancellationRequested)
        {
        }
        catch (Exception exception)
        {
            logger.LogWarning(
                exception,
                "Failed to load optional loader library versions. MinecraftVersion={MinecraftVersion} Loader={Loader}",
                requestedVersion,
                loaderOption!.Kind);
            if (IsAddonRequestCurrent(cancellation, loaderOption!, requestedVersion))
            {
                ClearAddonLibraryVersionOptions();
                AddonLibraryVersionLoadError = GetAddonLibraryVersionLoadFailedMessage(loaderOption!.Kind);
            }
        }
        finally
        {
            if (IsAddonRequestCurrent(cancellation, loaderOption!, requestedVersion))
                IsLoadingAddonLibraryVersions = false;
            ReleaseAddonCancellation(cancellation);
        }
    }

    private async Task RefreshExistingInstanceNamesAsync(CancellationToken cancellationToken)
    {
        instanceNameTracker.ReplaceExisting(await instanceService.GetInstancesAsync(cancellationToken));
        RefreshInstanceNameDuplicateMessage();
    }

    private void RefreshInstanceNameDuplicateMessage()
    {
        InstanceNameDuplicateMessage = !string.IsNullOrWhiteSpace(InstanceName?.Trim())
            && instanceNameTracker.IsUnavailable(InstanceName)
                ? Strings.Status_DuplicateInstanceName
                : string.Empty;
    }

    /// <summary>
    /// 仅在名称仍由系统管理时更新建议名称，避免覆盖用户手动输入。
    /// </summary>
    private void ApplyAutoGeneratedInstanceName(bool force)
    {
        var suggestedName = CreateSuggestedInstanceName();
        if (string.IsNullOrWhiteSpace(suggestedName))
            return;
        var currentName = InstanceName?.Trim() ?? string.Empty;
        // 只有强制初始化、空名称或仍等于上次建议值时才允许系统接管名称。
        if (!force
            && !string.IsNullOrWhiteSpace(currentName)
            && !string.Equals(currentName, lastAutoGeneratedInstanceName, StringComparison.OrdinalIgnoreCase))
        {
            return;
        }

        // 属性回调会同步执行，用 try/finally 保证异常时也不会永久进入“内部更新”状态。
        isUpdatingInstanceNameInternally = true;
        try
        {
            InstanceName = suggestedName;
            lastAutoGeneratedInstanceName = suggestedName;
        }
        finally
        {
            isUpdatingInstanceNameInternally = false;
        }
    }

    private string CreateSuggestedInstanceName()
    {
        var minecraftVersion = selectedMinecraftVersion?.Name?.Trim();
        if (string.IsNullOrWhiteSpace(minecraftVersion))
            return string.Empty;
        var loaderVersion = SelectedLoaderVersion?.Version;
        return SelectedLoaderOption?.Kind switch
        {
            LoaderKind.Fabric when !string.IsNullOrWhiteSpace(loaderVersion) => $"{minecraftVersion}-fabric-{loaderVersion}",
            LoaderKind.Fabric => $"{minecraftVersion}-fabric",
            LoaderKind.Forge when !string.IsNullOrWhiteSpace(loaderVersion) => $"{minecraftVersion}-forge-{loaderVersion}",
            LoaderKind.Forge => $"{minecraftVersion}-forge",
            LoaderKind.NeoForge when !string.IsNullOrWhiteSpace(loaderVersion) => $"{minecraftVersion}-neoforge-{loaderVersion}",
            LoaderKind.NeoForge => $"{minecraftVersion}-neoforge",
            LoaderKind.Quilt when !string.IsNullOrWhiteSpace(loaderVersion) => $"{minecraftVersion}-quilt-{loaderVersion}",
            LoaderKind.Quilt => $"{minecraftVersion}-quilt",
            _ => minecraftVersion
        };
    }

    private bool CanInstallSelectedLoader()
    {
        return SelectedLoaderOption?.Kind switch
        {
            LoaderKind.Vanilla => true,
            { } kind when RequiresLoaderVersionSelection(kind) => !IsLoadingLoaderVersions
                && (!SupportsAddonLibrarySelection(kind) || !IsLoadingAddonLibraryVersions)
                && string.IsNullOrWhiteSpace(LoaderVersionLoadError)
                && SelectedLoaderVersion is not null,
            _ => false
        };
    }

    private string GetSelectedLoaderDisplayName()
    {
        var version = SelectedLoaderVersion?.Version;
        if (RequiresLoaderVersionSelection(SelectedLoaderOption?.Kind) && !string.IsNullOrWhiteSpace(version))
            return $"{GetSelectedLoaderTitle()} {version}";
        return GetSelectedLoaderTitle();
    }

    private string GetSelectedLoaderTitle() => SelectedLoaderOption?.Title ?? Strings.Download_VanillaLoaderTitle;

    private Task<IReadOnlyList<ModrinthVersionInfo>> GetAddonLibraryVersionsAsync(
        LoaderKind loaderKind,
        string minecraftVersion,
        CancellationToken cancellationToken)
    {
        return loaderKind switch
        {
            LoaderKind.Fabric => modrinthService!.GetFabricApiVersionsAsync(minecraftVersion, cancellationToken),
            LoaderKind.Quilt => modrinthService!.GetQuiltStandardLibraryVersionsAsync(minecraftVersion, cancellationToken),
            _ => Task.FromResult<IReadOnlyList<ModrinthVersionInfo>>([])
        };
    }

    private static string GetAddonLibraryVersionLoadFailedMessage(LoaderKind loaderKind)
    {
        return loaderKind switch
        {
            LoaderKind.Fabric => Strings.Status_FabricApiVersionsLoadFailed,
            LoaderKind.Quilt => Strings.Status_QuiltLibraryVersionsLoadFailed,
            _ => string.Empty
        };
    }

    private CancellationTokenSource ReplaceLoaderRequestCancellation()
    {
        var next = new CancellationTokenSource();
        var previous = Interlocked.Exchange(ref loaderRequestCancellation, next);
        previous?.Cancel();
        return next;
    }

    private CancellationTokenSource ReplaceAddonRequestCancellation()
    {
        var next = new CancellationTokenSource();
        var previous = Interlocked.Exchange(ref addonRequestCancellation, next);
        previous?.Cancel();
        return next;
    }

    private void ReleaseLoaderCancellation(CancellationTokenSource cancellation)
    {
        Interlocked.CompareExchange(ref loaderRequestCancellation, null, cancellation);
        cancellation.Dispose();
    }

    private void ReleaseAddonCancellation(CancellationTokenSource cancellation)
    {
        Interlocked.CompareExchange(ref addonRequestCancellation, null, cancellation);
        cancellation.Dispose();
    }

    private bool IsLoaderRequestCurrent(
        CancellationTokenSource cancellation,
        DownloadLoaderOption loaderOption,
        string minecraftVersion)
    {
        return ReferenceEquals(loaderRequestCancellation, cancellation)
            && ReferenceEquals(SelectedLoaderOption, loaderOption)
            && string.Equals(selectedMinecraftVersion?.Name, minecraftVersion, StringComparison.Ordinal)
            && isActive;
    }

    private bool IsAddonRequestCurrent(
        CancellationTokenSource cancellation,
        DownloadLoaderOption loaderOption,
        string minecraftVersion)
    {
        return ReferenceEquals(addonRequestCancellation, cancellation)
            && ReferenceEquals(SelectedLoaderOption, loaderOption)
            && string.Equals(selectedMinecraftVersion?.Name, minecraftVersion, StringComparison.Ordinal)
            && isActive;
    }

    private void ResetLoaderVersionState(bool cancelRequest = true)
    {
        if (cancelRequest)
        {
            var cancellation = Interlocked.Exchange(ref loaderRequestCancellation, null);
            cancellation?.Cancel();
        }
        LoaderVersions.Clear();
        SelectedLoaderVersion = null;
        IsLoadingLoaderVersions = false;
        LoaderVersionLoadError = string.Empty;
        OnPropertyChanged(nameof(LoaderVersionPlaceholderText));
    }

    private void ResetAddonLibraryVersionState(bool cancelRequest = true)
    {
        if (cancelRequest)
        {
            var cancellation = Interlocked.Exchange(ref addonRequestCancellation, null);
            cancellation?.Cancel();
        }
        IsLoadingAddonLibraryVersions = false;
        AddonLibraryVersionLoadError = string.Empty;
        ClearAddonLibraryVersionOptions();
    }

    private void ClearAddonLibraryVersionOptions()
    {
        AddonLibraryVersions.Clear();
        SelectedAddonLibraryVersion = null;
    }

    private void CancelRequests()
    {
        var loaderCancellation = Interlocked.Exchange(ref loaderRequestCancellation, null);
        loaderCancellation?.Cancel();
        var addonCancellation = Interlocked.Exchange(ref addonRequestCancellation, null);
        addonCancellation?.Cancel();
    }

    private void NotifyLoaderCollectionChanged()
    {
        OnPropertyChanged(nameof(HasLoaderVersions));
        OnPropertyChanged(nameof(HasNoLoaderVersions));
        OnPropertyChanged(nameof(LoaderVersionPlaceholderText));
    }

    private void NotifyAddonCollectionChanged()
    {
        OnPropertyChanged(nameof(HasAddonLibraryVersions));
        OnPropertyChanged(nameof(IsAddonLibraryVersionSelectorEnabled));
        OnPropertyChanged(nameof(AddonLibraryVersionPlaceholderText));
        NotifyQuiltLibraryAliasPropertiesChanged();
    }

    private void NotifyQuiltLibraryAliasPropertiesChanged()
    {
        OnPropertyChanged(nameof(QuiltLibraryVersions));
        OnPropertyChanged(nameof(SelectedQuiltLibraryVersion));
        OnPropertyChanged(nameof(HasQuiltLibraryVersions));
        OnPropertyChanged(nameof(HasQuiltLibraryVersionLoadError));
        OnPropertyChanged(nameof(IsQuiltLibraryVersionSelectorEnabled));
        OnPropertyChanged(nameof(IsLoadingQuiltLibraryVersions));
        OnPropertyChanged(nameof(QuiltLibraryVersionPlaceholderText));
        OnPropertyChanged(nameof(QuiltLibraryVersionLoadError));
    }

    private void NotifyStateChanged()
    {
        OnPropertyChanged(nameof(CanInstall));
        InstallAvailabilityChanged?.Invoke();
    }

    private static bool RequiresLoaderVersionSelection(LoaderKind? loaderKind)
    {
        return loaderKind is LoaderKind.Fabric or LoaderKind.Forge or LoaderKind.NeoForge or LoaderKind.Quilt;
    }

    private static bool SupportsAddonLibrarySelection(LoaderKind? loaderKind)
    {
        return loaderKind is LoaderKind.Fabric or LoaderKind.Quilt;
    }
