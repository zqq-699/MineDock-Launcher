    public bool HasLoaderVersions => LoaderVersions.Count > 0;

    public bool HasNoLoaderVersions => !IsLoadingLoaderVersions
        && LoaderVersions.Count == 0
        && string.IsNullOrWhiteSpace(LoaderVersionLoadError)
        && RequiresLoaderVersionSelection(SelectedLoaderOption?.Kind)
        && isActive;

    public bool HasLoaderVersionLoadError => !string.IsNullOrWhiteSpace(LoaderVersionLoadError);

    public bool ShouldShowLoaderVersionSelector => isActive && RequiresLoaderVersionSelection(SelectedLoaderOption?.Kind);

    public bool ShouldShowAddonLibrarySelector => isActive && SupportsAddonLibrarySelection(SelectedLoaderOption?.Kind);

    public bool ShouldShowQuiltLibrarySelector => ShouldShowAddonLibrarySelector && SelectedLoaderOption?.Kind is LoaderKind.Quilt;

    public bool HasAddonLibraryVersions => AddonLibraryVersions.Count > 0;

    public bool HasQuiltLibraryVersions => HasAddonLibraryVersions;

    public bool HasAddonLibraryVersionLoadError => !string.IsNullOrWhiteSpace(AddonLibraryVersionLoadError);

    public bool HasQuiltLibraryVersionLoadError => HasAddonLibraryVersionLoadError;

    public bool IsAddonLibraryVersionSelectorEnabled => !IsLoadingAddonLibraryVersions && HasAddonLibraryVersions;

    public bool IsQuiltLibraryVersionSelectorEnabled => IsAddonLibraryVersionSelectorEnabled;

    public bool IsLoadingQuiltLibraryVersions => IsLoadingAddonLibraryVersions;

    public string AddonLibraryLabelText => SelectedLoaderOption?.Kind switch
    {
        LoaderKind.Fabric => Strings.Download_FabricApiLabel,
        LoaderKind.Quilt => Strings.Download_QuiltLibraryLabel,
        _ => string.Empty
    };

    public string LoaderVersionPlaceholderText => IsLoadingLoaderVersions
        ? string.Format(Strings.Status_LoadingLoaderVersionsFormat, GetSelectedLoaderTitle())
        : HasLoaderVersionLoadError
            ? Strings.Download_LoaderVersionLoadFailedShort
            : HasNoLoaderVersions
                ? string.Format(Strings.Download_LoaderVersionEmptyFormat, GetSelectedLoaderTitle())
                : Strings.Download_SelectVersionPlaceholder;

    public string AddonLibraryVersionPlaceholderText => IsLoadingAddonLibraryVersions
        ? Strings.Download_AddonLibraryLoading
        : HasAddonLibraryVersionLoadError
            ? Strings.Download_AddonLibraryLoadFailedShort
            : !HasAddonLibraryVersions
                ? Strings.Download_AddonLibraryEmpty
                : string.Empty;

    public string QuiltLibraryVersionPlaceholderText => AddonLibraryVersionPlaceholderText;

    public DownloadAddonLibraryVersionOption? SelectedQuiltLibraryVersion
    {
        get => SelectedAddonLibraryVersion;
        set => SelectedAddonLibraryVersion = value;
    }

    public string QuiltLibraryVersionLoadError => AddonLibraryVersionLoadError;

    public bool CanInstall => isActive
        && selectedMinecraftVersion is not null
        && CanInstallSelectedLoader()
        && !string.IsNullOrWhiteSpace(InstanceName)
        && !HasInstanceNameDuplicateMessage;

    /// <summary>
    /// 为选定 Minecraft 版本初始化安装选项，并加载实例名称占用情况。
    /// </summary>
    public async Task PrepareAsync(
        DownloadMinecraftVersionItem minecraftVersion,
        CancellationToken cancellationToken = default)
    {
        // 每次进入都从 Vanilla 基线开始，旧 Loader 选择不能泄漏到新的 Minecraft 版本。
        isActive = true;
        selectedMinecraftVersion = minecraftVersion;
        ResetLoaderVersionState();
        ResetAddonLibraryVersionState();
        SelectedLoaderOption = LoaderOptions.First(option => option.Kind is LoaderKind.Vanilla);
        ApplyAutoGeneratedInstanceName(force: true);
        await RefreshExistingInstanceNamesAsync(cancellationToken);
        NotifyStateChanged();
    }

    /// <summary>
    /// 结束当前选项会话，取消所有远端请求并清空与旧版本绑定的状态。
    /// </summary>
    public void Deactivate()
    {
        // 页面离开后所有选项结果都失去上下文，先使请求失效再清空状态，避免后台回写隐藏页面。
        isActive = false;
        selectedMinecraftVersion = null;
        CancelRequests();
        ResetLoaderVersionState();
        ResetAddonLibraryVersionState();
        lastAutoGeneratedInstanceName = string.Empty;
        NotifyStateChanged();
    }

    public void ApplyDownloadSourcePreference(DownloadSourcePreference preference)
    {
        if (downloadSourcePreference == preference)
            return;
        downloadSourcePreference = preference;
        if (isActive)
        {
            _ = RefreshLoaderVersionsForSelectionAsync(SelectedLoaderOption);
            _ = RefreshAddonLibraryVersionsForSelectionAsync(SelectedLoaderOption);
        }
    }

    public void ApplyDownloadSpeedLimit(int value)
    {
        downloadSpeedLimitMbPerSecond = Math.Max(value, 0);
    }

    /// <summary>
    /// 将已验证的页面选择冻结为安装请求；当前状态不可安装时返回空。
    /// </summary>
    public DownloadInstallRequest? CreateInstallRequest()
    {
        if (!CanInstall || selectedMinecraftVersion is null)
            return null;

        var selectedLoader = SelectedLoaderOption ?? LoaderOptions[0];
        var instanceName = string.IsNullOrWhiteSpace(InstanceName)
            ? selectedMinecraftVersion.Name
            : InstanceName.Trim();
        return new DownloadInstallRequest(
            selectedMinecraftVersion.Name,
            instanceName,
            selectedLoader.Kind,
            selectedLoader.Kind is LoaderKind.Vanilla ? null : SelectedLoaderVersion?.Version,
            selectedLoader.Kind is LoaderKind.Fabric ? SelectedAddonLibraryVersion?.VersionId : null,
            selectedLoader.Kind is LoaderKind.Quilt ? SelectedAddonLibraryVersion?.VersionId : null,
            GetSelectedLoaderDisplayName(),
            downloadSourcePreference,
            downloadSpeedLimitMbPerSecond);
    }

    public void NotifyNameAvailabilityChanged()
    {
        RefreshInstanceNameDuplicateMessage();
    }

    public void Dispose()
    {
        CancelRequests();
    }

    [RelayCommand]
    private void SelectLoaderOption(DownloadLoaderOption loaderOption)
    {
        SelectedLoaderOption = loaderOption;
    }

    partial void OnSelectedLoaderOptionChanged(DownloadLoaderOption? value)
    {
        // 集合项的 IsSelected 供模板使用，权威选择仍是 SelectedLoaderOption。
        foreach (var item in LoaderOptions)
            item.IsSelected = ReferenceEquals(item, value);
        OnPropertyChanged(nameof(HasNoLoaderVersions));
        OnPropertyChanged(nameof(ShouldShowLoaderVersionSelector));
        OnPropertyChanged(nameof(ShouldShowAddonLibrarySelector));
        OnPropertyChanged(nameof(ShouldShowQuiltLibrarySelector));
        OnPropertyChanged(nameof(AddonLibraryLabelText));
        // Loader 变化会影响建议名称和两类远端版本，但请求可并行启动。
        ApplyAutoGeneratedInstanceName(force: false);
        // 两类版本来自独立数据源并可并行刷新，各自用独立 CTS 保证最后一次选择生效。
        _ = RefreshLoaderVersionsForSelectionAsync(value);
        _ = RefreshAddonLibraryVersionsForSelectionAsync(value);
        NotifyStateChanged();
    }

    partial void OnSelectedLoaderVersionChanged(LoaderVersionInfo? value)
    {
        ApplyAutoGeneratedInstanceName(force: false);
        NotifyStateChanged();
    }

