    private readonly ILogger logger;
    // Loader 版本和附加库来自不同端点，独立 CTS 允许两类请求并行且互不误取消。
    private CancellationTokenSource? loaderRequestCancellation;
    private CancellationTokenSource? addonRequestCancellation;
    private DownloadMinecraftVersionItem? selectedMinecraftVersion;
    private DownloadSourcePreference downloadSourcePreference = DownloadSourcePreference.Auto;
    private int downloadSpeedLimitMbPerSecond;
    private string lastAutoGeneratedInstanceName = string.Empty;
    // 该标志区分系统建议名称和用户输入，防止属性回调把自动更新误判为手动覆盖。
    private bool isUpdatingInstanceNameInternally;
