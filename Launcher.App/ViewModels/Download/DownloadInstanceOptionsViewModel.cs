    internal DownloadInstanceOptionsViewModel(
        IGameInstanceService instanceService,
        IEnumerable<ILoaderProvider> loaderProviders,
        DownloadInstanceNameTracker instanceNameTracker,
        IModrinthService? modrinthService = null,
        ILogger? logger = null)
    {
        this.instanceService = instanceService;
        this.loaderProviders = loaderProviders.ToDictionary(provider => provider.Kind);
        this.instanceNameTracker = instanceNameTracker;
        this.modrinthService = modrinthService;
        this.logger = logger ?? NullLogger.Instance;
        LoaderOptions.Add(new DownloadLoaderOption(LoaderKind.Vanilla, Strings.Download_VanillaLoaderTitle, Strings.Download_VanillaLoaderSubtitle, string.Empty, "/Assets/Icons/block/grass_block.png"));
        LoaderOptions.Add(new DownloadLoaderOption(LoaderKind.Fabric, Strings.Download_FabricLoaderTitle, Strings.Download_FabricLoaderSubtitle, "\uE8B7", MinecraftVersionIconResolver.DefaultFabricIconSource));
        LoaderOptions.Add(new DownloadLoaderOption(LoaderKind.Forge, Strings.Download_ForgeLoaderTitle, Strings.Download_ForgeLoaderSubtitle, "\uE8B7", MinecraftVersionIconResolver.DefaultForgeIconSource));
        LoaderOptions.Add(new DownloadLoaderOption(LoaderKind.NeoForge, Strings.Download_NeoForgeLoaderTitle, Strings.Download_NeoForgeLoaderSubtitle, "\uE8B7", MinecraftVersionIconResolver.DefaultNeoForgeIconSource));
        LoaderOptions.Add(new DownloadLoaderOption(LoaderKind.Quilt, Strings.Download_QuiltLoaderTitle, Strings.Download_QuiltLoaderSubtitle, "\uE8B7", MinecraftVersionIconResolver.DefaultQuiltIconSource));
        LoaderVersions.CollectionChanged += (_, _) => NotifyLoaderCollectionChanged();
        AddonLibraryVersions.CollectionChanged += (_, _) => NotifyAddonCollectionChanged();
        SelectedLoaderOption = LoaderOptions[0];
    }

    public event Action? InstallAvailabilityChanged;

    public ObservableCollection<DownloadLoaderOption> LoaderOptions { get; } = [];

    public ObservableCollection<LoaderVersionInfo> LoaderVersions { get; } = [];

    public ObservableCollection<DownloadAddonLibraryVersionOption> AddonLibraryVersions { get; } = [];

    public ObservableCollection<DownloadAddonLibraryVersionOption> QuiltLibraryVersions => AddonLibraryVersions;

